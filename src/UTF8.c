void UTF8_Init()
{
    bUTF8 = FALSE;
    UTF_Buffer[0] = 0;
    UTF_Buffer[1] = 0;
    UTF_Buffer[2] = 0;
    UTF_Buffer[3] = 0;
    UTF_Seq = 0;
}

// https://www.utf8-chartable.de/unicode-utf8-table.pl
// U+2500->
inline void DoUTF8(u8 dummy)
{
    vu8 *PRX = &dummy;

    UTF_Buffer[UTF_Seq++] = *PRX;

    if (UTF_Seq >= 2)
    {
        u8 outchar = 0;

        if ((UTF_Buffer[0] >= 0x94) && (UTF_Buffer[0] <= 0x9C))
        {
            outchar = UTF_Table[UTF_Buffer[0]-UTF_OFFSET][UTF_Buffer[1]];
        }

        bUTF8 = FALSE;
        UTF_Seq = 0;
        UTF_Buffer[0] = 0;
        UTF_Buffer[1] = 0;

        TTY_PrintChar(outchar);
    }

    return;
}