void UTF8_Init()
{
    bUTF8 = FALSE;
    UTF_Buffer[0] = 0;
    UTF_Buffer[1] = 0;
    UTF_Buffer[2] = 0;
    UTF_Buffer[3] = 0;
    UTF_Seq = 0;
}

// https://www.utf8-chartable.de/unicode-utf8-table.pl
// U+2500->
inline void DoUTF8(u8 dummy)
{
    vu8 *PRX = &dummy;

    UTF_Buffer[UTF_Seq++] = *PRX;

    if (UTF_Seq >= 2)
    {
        u8 outchar = ' ';

        outchar = UTF_TablePtr[UTF_Buffer[0]][UTF_Buffer[1]];
        
        #ifdef UTF_LOGGING
        if ((outchar == 0) || (outchar == 0x20)) kprintf("Unfilled UTF8 character: $%X $%X", UTF_Buffer[0], UTF_Buffer[1]);
        #endif

        bUTF8 = FALSE;
        UTF_Seq = 0;
        UTF_Buffer[0] = 0;
        UTF_Buffer[1] = 0;

        TTY_PrintChar(outchar);
    }

    return;
}
