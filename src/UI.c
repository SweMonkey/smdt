/// @brief Apply UI theme
void UI_ApplyTheme()
{
    switch (sv_ThemeUI)
    {
        case 0: // Dark blue
            PAL_setColor(21, 0xA40);    // Window shadow
            PAL_setColor(22, 0xE80);    // Window border
        break;
        case 1: // Dark lime
            PAL_setColor(21, 0x0A4);    // Window shadow
            PAL_setColor(22, 0x0E8);    // Window border
        break;
        case 2: // Dark amber
            PAL_setColor(21, 0x04A);    // Window shadow
            PAL_setColor(22, 0x08E);    // Window border
        break;
        case 3: // High contrast
            PAL_setColor(21, 0x000);    // Window shadow
            PAL_setColor(22, 0xEEE);    // Window border
        break;
    
        default:
        break;
    }
}

/// @brief Start window composition
/// @param w Pointer to window to composite in
void UI_Begin(SM_Window *w)
{
    Target = w;
}

/// @brief Stop window composition and repaint it
void UI_End()
{
    UI_RepaintWindow();
    Target = NULL;
}

/// @brief Stop window composition
void UI_EndNoPaint()
{
    Target = NULL;
}

/// @brief Set window visibility
/// @param w Pointer to window
/// @param v Visibility (boolean)
